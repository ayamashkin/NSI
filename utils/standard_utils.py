"""
Standard normalization utilities.
Ensures canonical form for standards like ОСТ 1 34505-80 (with space between ОСТ and 1).
"""
import re
from typing import Optional


def canonicalize_standard(std: Optional[str]) -> str:
    """
    Привести обозначение стандарта к каноническому виду.

    Правила:
      - ОСТ1  → ОСТ 1  (пробел между ОСТ и 1)
      - ОСТ 1 → ОСТ 1  (уже канонично)
      - ГОСТ  → ГОСТ   (без изменений)
      - убираются лишние пробелы

    Examples:
      >>> canonicalize_standard("ОСТ1 34505-80")
      'ОСТ 1 34505-80'
      >>> canonicalize_standard("ОСТ 1 34505-80")
      'ОСТ 1 34505-80'
      >>> canonicalize_standard("ГОСТ 7798-70")
      'ГОСТ 7798-70'
    """
    if not std:
        return ''
    s = str(std).strip()
    # ОСТ1<пробел> или ОСТ1<цифра> → ОСТ 1 <цифра>
    s = re.sub(r'(?i)ОСТ1\s+', 'ОСТ 1 ', s)
    s = re.sub(r'(?i)ОСТ1(\d)', r'ОСТ 1 ', s)
    # ОСТ 1 с лишними пробелами → один пробел
    s = re.sub(r'(?i)ОСТ\s+1\s+', 'ОСТ 1 ', s)
    # ГОСТ — убрать лишние пробелы
    s = re.sub(r'(?i)ГОСТ\s+', 'ГОСТ ', s)
    # Общая чистка пробелов
    s = re.sub(r'\s+', ' ', s)
    return s.strip()